				<div class="col s12 m12 l12" id="ceta-ognjenega-ruja-komen">
					<span class="section-title red-text text-darken-4">Četa Ognjenega ruja Komen</span>
					<p>
						V času, ko je v Sežani deloval Odred Srečka Kosovela, so bili taborniki prisotni skoraj v vsakem večjem kraju na Krasu in Komen ni bil izjema. Kmalu po ponovnem zagonu taborništva v Sežani leta 1999 se je tako pri Komencih vzbudila želja po ponovni obuditvi gibanja tudi pri njih.
					</p>
					<p>
						Prvi poskusi so se tako zgodili že takoj na začetku, ko je v šolskem letu 1999/2000 s prvimi vodi na osnovni šoli Antona Šibelja - Stjenka Komen začela delati Andreja Trobec iz Dolenje vasi, ki je tam delala kot učiteljica. Proti koncu leta se ji je pridružil tudi Andrej Vidmar, prostovoljec iz Komna, in skupaj sta vodila 2 voda. Komenci so se nam tedaj tudi pridružili na taboru ob Nadiži, toda ne preveč navdušeno in številčno, četa pa tudi ni dobila pravega vodstva. Ni se pravočasno poskrbelo za vzgojo novih kadrov in z odhodom Andreje z osnovne šole je vse skupaj kmalu zamrlo.
					</p>
					<p>
						Vse razen ideje in želje. Komenci so vztrajali, nas spraševali, vabili. Leta 2006 smo tako začeli ponovno iskati kandidate, ki bi bili pripravljeni prestati izobraževanje in začeti z delom. S predstavitvami na občinskih praznikih v Komnu nismo bili preveč uspešni, vsaj pri iskanju vodnikov ne. Odločili smo se za »agresivnejši« pristop. Špela Rožman je na Srednji šoli Srečka Kosovela v Sežani dobesedno podkupila 3 mlade z obljubami o norih brezplačnih počitnicah z njihovimi vrstniki. Na taboru ob Lahinji leta 2008 so se nam takrat pridružili Janja Uršič, Jaka Peric, Ivan Stančič in njegov mlajši brat Andrej. Pripravili smo jim en »šnel kurs« taborništva, ki smo ga poimenovali Gozdna šola. Ta je kasneje pri nas postala stalna praksa, a to je že druga zgodba.
					</p>
					<p>
						Jaka, Janja in Ivan so v šolskem letu 2008/09 začeli delati s prvimi vodi, Ivanova mama Ivana Stančič je sprejela nalogo prve starešine Komenske čete, Samo Gec iz Sežane pa jim je kot načelnik pomagal pri prvih korakih. Četa je bila ustanovljena na rednem občnem zboru 27. februarja 2009 in je na ta dan štela 28 članov. Ime, ki so ga prevzeli – Četa ognjenega ruja Komen, je močno povezano z našim društvom, saj je bilo ime Rod ognjenega ruja Sežana eno od treh možnosti, ko smo ime izbirali Sežanci.
					</p>
					<p>
						Z naslednjim letom sta se Ivan in Ivana umaknila iz društva, sta pa zato k nam prišli dve zelo zagnani punci: Irena Terčon in Martina Suban. Prva je postala nova starešina, druga pa načelnica. Janja je skupaj z Martino vodila MČ vod, Jaka je še naprej soliral z GG-ji. Da jim gre dobro, dokazuje njihova številčnost, ki ostaja vse od ustanovitve precej konstantna. Vpisujejo nove člane, vzgajajo nove kadre, obiskujejo velika tekmovanja. Leta 2009 so bili GG-ji s tretjim mestom zelo uspešni na državnem mnogoboju v Zrečah.
					</p>
					<p>
						Četa ima definitivno svetlo prihodnost, mogoče kmalu celo kot samostojno društvo. Naj ob tem poudarim, da je vse od ustanovitve Četa uživala močno podporo Občine Komen, OŠ Antona Šibelja - Stjenka, Razvojnega, kulturnega, turističnega, športnega društva Gorjansko in vaške skupnosti Gorjansko.
					</p>
