				<div class="col s12 m12 l12" id="ceta-crnega-bora-divaca">
					<span class="section-title red-text text-darken-4">Četa Črnega bora Divača</span>
					<p>
						V letih 1960 do 1980 je v Divači deloval odred tabornikov Janka Premrla Vojka, ki je bil tudi prvi med odredi, ki so delovali na območju bivše občine Sežana. Le ta je vzpodbudil ustanavljanje odredov v večjih krajih v občini. Tako so v temu času delovali odredi v Sežani, Hrpeljah in Komnu. Taborniška dejavnost je za nekaj časa zamrla, dokler niso Sežanci ponovno obudili gibanje, ki združuje mlade in tudi starejše, ki imajo radi naravo in taborniško življenje.
					</p>
					<p>
						Kot bivša tabornica (bila sem članica odreda Srečka Kosovela iz Sežane) sem se razveselila vzpodbude Sežancev, da obudimo in ponovno ustanovimo tabornike tudi v Divači. Ustanovitvi tabornikov je naklonjen tudi župan občine Divača Drago Božac. Zaenkrat je naša divaška četa še del Roda Kraških J'rt iz Sežane, upamo pa, da bomo nekega dne spet »veliki« in samostojni.
					</p>
					<p>
						Od konca septembra 2011 v Divači deluje taborniška Četa črnega bora. Vse se je začelo pred poletnimi počitnicami, ko so taborniki iz Sežane predstavili svoje delovanje na osnovni šoli v Divači in otrokom ponudili možnost za vpis ter se predstavili na prazniku Občine Divača. Na začetku poletnih počitnic, 10. julija 2011 sta se Špela in Maša pridružili sežanskim tabornikom na njihovem vsakoletnem taboru v Geršičih pri Lahinji, kjer sta preživeli zanimivih 10 dni ob spoznavanju novih ljudi in njima povsem novega taborništva. Mesec po taboru sta se udeležili še vodniškega tečaja na Jagodju pri Izoli, kjer sta dobili veliko znanja in novih izkušenj, ki jima bodo prišle prav pri učenju in motiviranju članov voda.
					</p>
