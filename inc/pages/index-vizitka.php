					<span class="section-title red-text text-darken-4">Taborniška hiška</span>
					<p>
						Sredi parka v Sežani stoji hiška, ki smo jo taborniki leta 2009 ponovno dobili v uporabo. Hiška nastala na temeljih grajske lope, ki so jo pridni taborniki Odreda Srečka Kosovela v sedemdesetih letih obnovili in razširili. Danes je hiška zbirališče tabornikov in nudi zatočišče številnim skavtom iz Evrope in Slovenije, ki se predvsem v poletnih mesecih potikajo po Krasu.
					</p>
					<p>
						Seveda pa je tabornik pripravljen pomagati in tako smo svoj domek pripravljeni "posojati" neprofitnim organizacijam in društvom. Hiška je na voljo, kadar v njej ne potekajo naši sestanki in srečanja, za termine se posvetujte z urnikom. V primeru izposoje se poravna le prispevek za čiščenje in pozimi za ogrevanje, po želji pa ta izposoja lahko poteka vse leto v določenem terminu. Vse zainteresirane prosimo, naj si najprej preberejo naš hišni red in se ga nato tudi vestno držijo.
					</p>
					<p>
						Čarobni svet naše hiške je sestavljen iz dnevnega prostora, kjer imamo večjo mizo in stole, kavč, manjše mizice in goro blazin. Koristite lahko tudi tablo, ena stena pa je prazna in je namenjena projekcijam. Fotografije naših lepotcev in lepotic na stenah si lahko ogledujete brez doplačila. V hiški so tudi sanitarije in manjša kuhinja (plinski štedilnik, električna pečica, hladilnik, pomivalno korito), ki je opremljena z veliko mizo in klopjo.
					</p>
